import requests
import io
from PIL import Image
import pytesseract
from transformers import BlipProcessor, BlipForConditionalGeneration, pipeline
import fitz  # PyMuPDF for PDF processing
import os  # For checking file existence
from requests.exceptions import HTTPError
# Initialize Tesseract for OCR
pytesseract.pytesseract.tesseract_cmd = r'/usr/bin/tesseract'  # Adjust the path to your Tesseract executable

# Initialize the image captioning model and processor (BLIP)
feature_extractor = BlipProcessor.from_pretrained("Salesforce/blip-image-captioning-base")
model = BlipForConditionalGeneration.from_pretrained("Salesforce/blip-image-captioning-base")
tokenizer = feature_extractor.tokenizer

# Initialize a GPT-based text generation pipeline (e.g., from Hugging Face Transformers)
gpt_pipeline = pipeline("text-generation", model="gpt2")

def enhance_text_with_gpt(input_text):
    """Enhances the extracted text using GPT for a detailed explanation.

    Args:
        input_text: The text extracted from an image or PDF.

    Returns:
        A detailed explanation generated by GPT.
    """
    prompt = f"Provide a detailed explanation or context for the following text: \"{input_text}\""
    # Use max_new_tokens to control the length of generated text
    enhanced_text = gpt_pipeline(prompt, max_new_tokens=100, num_return_sequences=1)[0]['generated_text']
    return enhanced_text

def extract_text_and_images_from_pdf(pdf_source):
    """Extracts text and images from a PDF using PyMuPDF and provides detailed descriptions for images.

    Args:
        pdf_source: URL or local path of the PDF file.

    Returns:
        A dictionary containing extracted text and detailed descriptions of images.
    """
    try:
        # Attempt to fetch the PDF from the URL
        response = requests.get(pdf_source)
        response.raise_for_status()  # Raise HTTPError for bad responses
        pdf_document = fitz.open(io.BytesIO(response.content))
    except (requests.exceptions.RequestException, HTTPError):
        # If URL access fails, assume it's a local file path
        # Check if the file exists and open it directly with fitz
        if os.path.isfile(pdf_source):
            pdf_document = fitz.open(pdf_source)
        else:
            raise FileNotFoundError(f"Could not access PDF at: {pdf_source}")

    result = {"text": "", "image_descriptions": []}

    # Extract text and images from all pages
    for page_num in range(pdf_document.page_count):
        page = pdf_document.load_page(page_num)
        extracted_text = page.get_text()
        result["text"] += extracted_text

        # Enhance text with GPT for detailed explanations
        if extracted_text.strip():
            detailed_text = enhance_text_with_gpt(extracted_text)
            result["text"] += f"\n\nEnhanced Explanation:\n{detailed_text}\n"

        # Extract images from the page
        images = page.get_images(full=True)
        for img_index, img in enumerate(images):
            xref = img[0]
            base_image = pdf_document.extract_image(xref)
            image_bytes = base_image["image"]
            image = Image.open(io.BytesIO(image_bytes))

            # Generate a detailed description for the image
            pixel_values = feature_extractor(images=image, return_tensors="pt").pixel_values
            generated_ids = model.generate(pixel_values, max_length=50, num_beams=4)
            description = tokenizer.batch_decode(generated_ids, skip_special_tokens=True)[0]

            result["image_descriptions"].append({
                "page": page_num + 1,
                "description": description
            })

    return result

def generate_text_from_image(image_url):
    """Converts an image to text

    Args:
        image_url: URL of the image file.

    Returns:
        A text output describing or extracting text from the image.
    """
    # Fetch the image from the URL
    response = requests.get(image_url, stream=True)
    response.raise_for_status()
    image = Image.open(io.BytesIO(response.content))

    # First, try extracting text using OCR (for images with textual content like textbook pages)
    extracted_text = pytesseract.image_to_string(image)

    # If OCR returns meaningful text, enhance it with GPT
    if extracted_text.strip():
        detailed_text = enhance_text_with_gpt(extracted_text)
        return f"Extracted Text:\n{extracted_text}\n\nEnhanced Explanation:\n{detailed_text}"

    # If no meaningful text, use image captioning model to describe the image (for images like pictures)
    # Prepare the image for the model
    pixel_values = feature_extractor(images=image, return_tensors="pt").pixel_values

    # Generate the caption with more details
    generated_ids = model.generate(pixel_values, max_length=50, num_beams=4)
    generated_text = tokenizer.batch_decode(generated_ids, skip_special_tokens=True)[0]

    return generated_text

def process_input(input_source):
    """Determines whether the input is a PDF or an image URL and processes accordingly.

    Args:
        input_source: URL or local path of the PDF or image.

    Returns:
        Extracted text or generated description.
    """
    if input_source.lower().endswith(('.pdf')):
        # Process as PDF
        return extract_text_and_images_from_pdf(input_source)
    else:
        # Process as Image
        return generate_text_from_image(input_source)

