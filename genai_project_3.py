import matplotlib.pyplot as plt

def plot_accuracy_metrics(accuracy_results):
    """Plots accuracy metrics from a dictionary.

    Args:
        accuracy_results: A dictionary containing accuracy metrics.
    """

    metrics_to_plot = ["levenshtein_distance", "levenshtein_ratio", "char_accuracy",
                       "word_accuracy", "precision", "recall", "f1_score"]

    # Extract metric names and values
    metric_names = []
    metric_values = []
    for metric in metrics_to_plot:
        if metric in accuracy_results:  # Check if the metric exists
            metric_names.append(metric)
            metric_values.append(accuracy_results[metric])


    # Create the plot
    plt.figure(figsize=(10, 6))  # Adjust figure size as needed
    plt.plot(metric_names, metric_values, marker='o', linestyle='-')
    plt.xlabel("Metrics")
    plt.ylabel("Values")
    plt.title("Accuracy Metrics")

    # Annotate each point with its value
    for i, value in enumerate(metric_values):
        plt.annotate(f"{value:.4f}", (metric_names[i], value), textcoords="offset points", xytext=(0,10), ha='center')

    plt.grid(True)
    plt.xticks(rotation=45, ha='right') # Rotate x-axis labels for better readability
    plt.tight_layout() # Adjust layout to prevent labels from overlapping
    plt.show()


